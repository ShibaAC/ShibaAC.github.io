// 开始测验
function startTest() {
    if (allQuestions.length === 0) {
        alert('题目尚未加载完成，请稍候再试');
        return;
    }
    
    // 随机选择80题
    const shuffled = [...allQuestions].sort(() => Math.random() - 0.5);
    selectedQuestions = shuffled.slice(0, 100);
    
    // 初始化答案
    userAnswers = {};
    currentQuestionIndex = 0;
    timeRemaining = 1200 * 60;
    isTestStarted = true;
    isTestFinished = false;
    
    // 显示计时器
    document.getElementById('timer').style.display = 'block';
    
    // 启动计时器
    startTimer();
    
    // 显示第一题
    showQuestion();
}

// 启动计时器
function startTimer() {
    timerInterval = setInterval(() => {
        timeRemaining--;
        updateTimer();
        
        if (timeRemaining <= 0) {
            clearInterval(timerInterval);
            submitTest();
        }
    }, 1000);
    updateTimer();
}

// 更新计时器显示
function updateTimer() {
    const minutes = Math.floor(timeRemaining / 60);
    const seconds = timeRemaining % 60;
    const timerEl = document.getElementById('timer');
    timerEl.textContent = `${String(minutes).padStart(2, '0')}:${String(seconds).padStart(2, '0')}`;
    
    // 警告样式
    timerEl.classList.remove('warning', 'danger');
    if (timeRemaining <= 300) { // 5分钟
        timerEl.classList.add('danger');
    } else if (timeRemaining <= 600) { // 10分钟
        timerEl.classList.add('warning');
    }
}

// 显示题目
function showQuestion() {
    if (currentQuestionIndex >= selectedQuestions.length) {
        submitTest();
        return;
    }
    
    const question = selectedQuestions[currentQuestionIndex];
    const progress = ((currentQuestionIndex + 1) / selectedQuestions.length * 100).toFixed(1);
    
    let optionsHtml = '';
    question.options.forEach(opt => {
        const isSelected = userAnswers[question.id] === opt.label;
        optionsHtml += `
            <li class="option ${isSelected ? 'selected' : ''}" onclick="selectOption('${opt.label}')">
                <input type="radio" name="answer" value="${opt.label}" ${isSelected ? 'checked' : ''}>
                <strong>${opt.label}.</strong> ${opt.text}
            </li>
        `;
    });
    
    const content = document.getElementById('content');
    content.innerHTML = `
        <div class="progress-text">第 ${currentQuestionIndex + 1} / ${selectedQuestions.length} 題</div>
        <div class="progress-bar">
            <div class="progress-fill" style="width: ${progress}%"></div>
        </div>
        
        <div class="question-container">
            <div class="question-header">
                <span class="question-number">題目 ${currentQuestionIndex + 1}</span>
                <span class="chapter-tag">${question.chapter}</span>
            </div>
            <div class="question-text">${question.question}</div>
            <ul class="options">
                ${optionsHtml}
            </ul>
        </div>
        
        <div class="button-group">
            <button class="btn-secondary" onclick="previousQuestion()" ${currentQuestionIndex === 0 ? 'disabled' : ''}>
                上一題
            </button>
            <button class="btn-secondary" onclick="nextQuestion()">
                ${currentQuestionIndex === selectedQuestions.length - 1 ? '提交答案' : '下一題'}
            </button>
        </div>
    `;
}

// 选择选项
function selectOption(answer) {
    const question = selectedQuestions[currentQuestionIndex];
    userAnswers[question.id] = answer;
    showQuestion(); // 重新渲染以更新选中状态
}

// 上一题
function previousQuestion() {
    if (currentQuestionIndex > 0) {
        currentQuestionIndex--;
        showQuestion();
    }
}

// 下一题
function nextQuestion() {
    if (currentQuestionIndex < selectedQuestions.length - 1) {
        currentQuestionIndex++;
        showQuestion();
    } else {
        submitTest();
    }
}

// 提交测验
function submitTest() {
    if (isTestFinished) return;
    
    isTestFinished = true;
    clearInterval(timerInterval);
    
    // 计算分数
    let correctCount = 0;
    const wrongQuestions = [];
    
    selectedQuestions.forEach(question => {
        const userAnswer = userAnswers[question.id];
        const isCorrect = userAnswer === question.answer;
        
        if (isCorrect) {
            correctCount++;
        } else {
            wrongQuestions.push({
                question: question,
                userAnswer: userAnswer || '未作答',
                correctAnswer: question.answer
            });
        }
    });
    
    const score = Math.round((correctCount / selectedQuestions.length) * 100);
    
    // 显示结果
    showResults(score, correctCount, selectedQuestions.length, wrongQuestions);
}

// 显示结果
function showResults(score, correctCount, totalCount, wrongQuestions) {
    let wrongQuestionsHtml = '';
    
    if (wrongQuestions.length > 0) {
        wrongQuestionsHtml = '<h3 style="margin-top: 30px; color: #333;">錯誤題目詳解：</h3><div class="wrong-questions">';
        
        wrongQuestions.forEach((item, index) => {
            const q = item.question;
            const optionsHtml = q.options.map(opt => {
                let style = '';
                if (opt.label === item.correctAnswer) {
                    style = 'color: #28a745; font-weight: bold;';
                } else if (opt.label === item.userAnswer && item.userAnswer !== item.correctAnswer) {
                    style = 'color: #dc3545; font-weight: bold;';
                }
                return `<div style="${style}"><strong>${opt.label}.</strong> ${opt.text}</div>`;
            }).join('');
            
            wrongQuestionsHtml += `
                <div class="wrong-question-item">
                    <div class="question-header">
                        <span class="question-number">錯誤題目 ${index + 1}</span>
                        <span class="chapter-tag">${q.chapter}</span>
                    </div>
                    <div class="question-text">${q.question}</div>
                    <div class="answer-display">
                        ${optionsHtml}
                        <div style="margin-top: 15px; padding-top: 15px; border-top: 1px solid #ddd;">
                            <span class="wrong-answer">您的答案: ${item.userAnswer || '未作答'}</span> | 
                            <span class="correct-answer">正確答案: ${item.correctAnswer}</span>
                        </div>
                    </div>
                </div>
            `;
        });
        
        wrongQuestionsHtml += '</div>';
    } else {
        wrongQuestionsHtml = '<div style="margin-top: 30px; padding: 20px; background: #d4edda; border-radius: 8px; color: #155724;"><strong>恭喜！您全部答對了！</strong></div>';
    }
    
    const content = document.getElementById('content');
    content.innerHTML = `
        <div class="result-container">
            <h2>測驗完成！</h2>
            <div class="score">${score} 分</div>
            <div class="result-summary">
                答對 ${correctCount} 題 / 共 ${totalCount} 題
            </div>
            ${wrongQuestionsHtml}
            <div style="margin-top: 30px;">
                <button class="btn-primary" onclick="location.reload()">重新測驗</button>
            </div>
        </div>
    `;
    
    // 隐藏计时器
    document.getElementById('timer').style.display = 'none';
}
