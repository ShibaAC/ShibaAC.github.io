function parseQuestions(text) {
    const questions = [];
    const lines = text.split('\n');
    let currentChapter = '';
    let currentChapterNum = 0;
    let i = 0;
    
    while (i < lines.length) {
        let line = lines[i].trim();
        if (!line) { i++; continue; }

        // 🔹 檢查章節標題
        if (/^第[一二三四五六七八九十]+章/.test(line)) {
            currentChapter = line.trim();
            const chapterMatch = line.match(/第([一二三四五六七八九十]+)章/);
            const chapterNumMap = { 一:1, 二:2, 三:3, 四:4, 五:5, 六:6, 七:7, 八:8, 九:9, 十:10 };
            if (chapterMatch) {
                currentChapterNum = chapterNumMap[chapterMatch[1]] || 0;
            }
            i++;
            continue;
        }

        // 🔹 檢查是否為題目開頭，例如 "(A) 3. ..." 
        const qMatch = line.match(/^\(([A-D])\)\s*(\d+)\.\s*(.+)$/);
        if (qMatch) {
            const answer = qMatch[1];
            const qNum = parseInt(qMatch[2]);
            console.log(qNum);
            let qText = qMatch[3];
            const options = [];
            i++;

            // 讀取接下來的選項
            while (i < lines.length) {
                const nextLine = lines[i].trim();
                if (!nextLine) { i++; continue; }

                // 🔹 檢查是否為下一題
                if (/^\([A-D]\)\s*\d+\./.test(nextLine)) break;
                // 🔹 檢查是否為新章節
                if (/^第[一二三四五六七八九十]+章/.test(nextLine)) break;

                // 🔹 檢查是否為選項
                const optMatch = nextLine.match(/^\(([A-D])\)\s*(.+)$/);
                if (optMatch) {
                    options.push({
                        label: optMatch[1],
                        text: optMatch[2]
                    });
                } else {
                    // 可能是題目文字延續行
                    qText += ' ' + nextLine;
                }
                i++;
            }
            if (options.length > 0) {
                questions.push({
                    id: qNum,
                    chapter: currentChapter,
                    chapterNum: currentChapterNum,
                    question: qText,
                    options: options,
                    answer: answer
                });
            } else {
                console.warn('⚠️ 找不到選項：', qText);
            }
            continue;
        }
        i++;
    }
    
    return questions;
}

